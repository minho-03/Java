package Test;

public class StringCaseConversion_9 {

	public static void main(String[] args) {
		String text = "GoodHycu";
		
		String upperText = text.toUpperCase(); // 대문자로 변환
		String lowerText = text.toLowerCase(); // 소문자로 변환
		
		System.out.println("대문자: " + upperText);
		System.out.println("소문자: " + lowerText);
		
	}

}
